# This is a basic datatypes in python called primitive datatype.
# Python is dynamic typed language
# Using format method to modify string
# 1.integer
# 2.float
# 3.string
# 4.boolean

def main():
    # integer value
    year = 2022
    print("This year is :{}".format(year))

    # float value
    pi = 3.14
    print("This is :{}".format(pi))

    # string value
    program_version = "python 3.7"
    print("The Program version :{}".format(program_version))

    # boolean value
    file_exists = False
    print("This is :{}".format(file_exists))


if __name__ == '__main__':
    main()
